package ex_09_ForLoop;

public class Lab67_Long_Float_Loop {
    public static void main(String[] args) {
        for ( float f=12.45f;f<123.56f;f++){// Float data type valid but not used in loop
            System.out.println(f);
        }
        for (long l=1234l;l<1678l;l++){// Long data type valid but not used in loop

            System.out.println(l);
        }
    }
}
